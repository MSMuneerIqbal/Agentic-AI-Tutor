    # Initialize Pinecone
    try:
        from pinecone import Pinecone
        pc = Pinecone(api_key=os.getenv("PINECONE_API_KEY"))
        index = pc.Index("docker-kubernetes-tutor")
        print("✅ Connected to Pinecone")
    except Exception as e:
        print(f"❌ Pinecone connection failed: {e}")
        return False
    
